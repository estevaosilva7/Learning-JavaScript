// == Ver se o dado é igual
// === Ver se o tipo de dado é igual
// !== Ver a diferença do tipo de dado

var numero = 1;

if(numero === 7) {
    console.log("Esse é o número 7 do tipo number");
} 
else if(numero === "7") 
{   console.log("Esse número 7 é do tipo string");
}
else(numero !== 7) 
{   console.log("Esse não é o 7 de nenhum tipo")
}